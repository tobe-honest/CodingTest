input = sys.stdin.readline
N = int(input())
memo = [0 for i in range(N+1)]
memo[1] = 1
if N > 1: memo[2] = 2

for i in range(3, N+1):
    memo[i] = (memo[i-2] + memo[i-1]) % 15746

print(memo[N] % 15746)


